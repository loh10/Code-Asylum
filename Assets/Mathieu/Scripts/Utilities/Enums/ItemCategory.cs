/// <summary>
/// Categorizes items for organizational and analytical purposes.
/// </summary>
public enum ItemCategory
{
    /// <summary>
    /// Items tied to quests or story progression.
    /// </summary>
    Quest,

    /// <summary>
    /// Tools, keys, and similar items
    /// </summary>
    Utility,

    /// <summary>
    /// Optional or decorative items.
    /// </summary>
    Collectible,

    /// <summary>
    /// Rare or unique items with special properties.
    /// </summary>
    Special,

    /// <summary>
    /// A generic category for items that don't fit elsewhere.
    /// </summary>
    Miscellaneous
}